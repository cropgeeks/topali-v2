    private void bOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bOkActionPerformed
        aStart = (Integer)start.getValue();
        aEnd = (Integer)end.getValue();
        
        if((aEnd-aStart+1)%3!=0) {
        	MsgBox.msg("The length of a coding region must be dividable by 3.", MsgBox.ERR);
        	return;
        }
        
        aM1 = (String)m1Type.getSelectedItem();
        aM1Gamma = m1Gamma.isSelected();
        aM1Inv = m1Inv.isSelected();
        aM2 = (String)m2Type.getSelectedItem();
        aM2Gamma = m2Gamma.isSelected();
        aM2Inv = m2Inv.isSelected();
        aM3 = (String)m3Type.getSelectedItem();
        aM3Gamma = m3Gamma.isSelected();
        aM3Inv = m3Inv.isSelected();
        
        aComment = comment.getText();
        if(aComment.trim().equals(""))
            aComment = null;
        
        cancelled = false;
        
        setVisible(false);
    }//GEN-LAST:event_bOkActionPerformed

