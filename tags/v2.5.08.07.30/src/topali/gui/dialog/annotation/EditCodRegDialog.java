    public void init(CodingRegionAnnotation anno, AlignmentData data) {
    	if(anno!=null) {
    		SpinnerNumberModel mod1 = new SpinnerNumberModel(anno.getStart(), 1, data.getSequenceSet().getLength(), 1);
    		start.setModel(mod1);
    		SpinnerNumberModel mod2 = new SpinnerNumberModel(anno.getEnd(), 1, data.getSequenceSet().getLength(), 1);
    		end.setModel(mod2);
    		if(anno.getComment()!=null)
    			comment.setText(anno.getComment());
    		
    		List<Model> models = ModelManager.getInstance().listDNAModels();
    		
    		if(models!=null) {
    			String[] modNames = new String[models.size()];
    			int sel1=-1, sel2=-1, sel3=-1;
    			for(int i=0; i<modNames.length; i++) {
    				modNames[i] = models.get(i).getName();
    				
    				if(modNames[i].equals(anno.getModC1().getName()))
    					sel1 = i;
    				if(modNames[i].equals(anno.getModC2().getName()))
    					sel2 = i;
    				if(modNames[i].equals(anno.getModC3().getName()))
    					sel3 = i;
    			}
    			DefaultComboBoxModel mod3 = new DefaultComboBoxModel(modNames);
    			DefaultComboBoxModel mod4 = new DefaultComboBoxModel(modNames);
    			DefaultComboBoxModel mod5 = new DefaultComboBoxModel(modNames);
    			m1Type.setModel(mod3);
    			m2Type.setModel(mod4);
    			m3Type.setModel(mod5);

    			if(sel1>-1)
    				m1Type.setSelectedIndex(sel1);
    			if(sel2>-1)
    				m2Type.setSelectedIndex(sel2);
    			if(sel3>-1)
    				m3Type.setSelectedIndex(sel3);
    			
    			m1Gamma.setSelected(anno.getModC1().isGamma());
    			m1Inv.setSelected(anno.getModC1().isInv());
    			m2Gamma.setSelected(anno.getModC2().isGamma());
    			m2Inv.setSelected(anno.getModC2().isInv());
    			m3Gamma.setSelected(anno.getModC3().isGamma());
    			m3Inv.setSelected(anno.getModC3().isInv());
    		}
    	}
    	
    	else {
    		SpinnerNumberModel mod1 = new SpinnerNumberModel(1, 1, data.getSequenceSet().getLength(), 1);
    		start.setModel(mod1);
    		SpinnerNumberModel mod2 = new SpinnerNumberModel(data.getSequenceSet().getLength(), 1, data.getSequenceSet().getLength(), 1);
    		end.setModel(mod2);
    		
    		List<Model> models = null;
    		if(data.getSequenceSet().getProps().isNucleotides()) {
    			models = ModelManager.getInstance().listDNAModels();
    		}
    		else if(data.getSequenceSet().getProps().isProtein()) {
    			models = ModelManager.getInstance().listProteinModels();
    		}
    		
    		if(models!=null) {
    			Model defModel = data.getSequenceSet().getProps().getModel();
    			String[] modNames = new String[models.size()];
    			int sel=-1;
    			for(int i=0; i<modNames.length; i++) {
    				modNames[i] = models.get(i).getName();
    				
    				if(modNames[i].equals(defModel.getName()))
    					sel = i;
    			}
    			DefaultComboBoxModel mod3 = new DefaultComboBoxModel(modNames);
    			DefaultComboBoxModel mod4 = new DefaultComboBoxModel(modNames);
    			DefaultComboBoxModel mod5 = new DefaultComboBoxModel(modNames);
    			m1Type.setModel(mod3);
    			m2Type.setModel(mod4);
    			m3Type.setModel(mod5);
    			
    			if(sel>-1) {
    				m1Type.setSelectedIndex(sel);
    				m2Type.setSelectedIndex(sel);
    				m3Type.setSelectedIndex(sel);
    			}
    			
    			m1Gamma.setSelected(defModel.isGamma());
    			m1Inv.setSelected(defModel.isInv());
    			m2Gamma.setSelected(defModel.isGamma());
    			m2Inv.setSelected(defModel.isInv());
    			m3Gamma.setSelected(defModel.isGamma());
    			m3Inv.setSelected(defModel.isInv());
    		}
    	}
    }
    
